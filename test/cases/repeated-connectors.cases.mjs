// カウントしない。これは `same-ending` と同じ節境界判定 (sentence.structureSectionIndex)
// を共有する。
//
// expect: true  は repeated-connectors が発火すべき、
// expect: false は発火してはならない、
// expect: { count: N } は N 件の指摘が立つべき。

